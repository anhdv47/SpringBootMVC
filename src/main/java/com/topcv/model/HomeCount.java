package com.topcv.model;

public class HomeCount {
    private int numberCandidate;
    private int numberCompany;
    private int numberRecruitment;

    public int getNumberCandidate() {
        return numberCandidate;
    }

    public void setNumberCandidate(int numberCandidate) {
        this.numberCandidate = numberCandidate;
    }

    public int getNumberCompany() {
        return numberCompany;
    }

    public void setNumberCompany(int numberCompany) {
        this.numberCompany = numberCompany;
    }

    public int getNumberRecruitment() {
        return numberRecruitment;
    }

    public void setNumberRecruitment(int numberRecruitment) {
        this.numberRecruitment = numberRecruitment;
    }
}
